#include "SignalGuard.h"

#include "Utils.h"


namespace qtlib {

/**************************************************************************************************
*   public
*
**************************************************************************************************/

//-------------------------------------------------------------------------------------------------
SignalGuard::SignalGuard(
    QObject    *a_target,
    const char *a_slot
) :
   _isEmit(true)
{
   connect(this,     SIGNAL( trigger() ),
           a_target, a_slot);
}
//-------------------------------------------------------------------------------------------------
SignalGuard::~SignalGuard()
{
   if (_isEmit) {
      emit trigger();
   }
}
//-------------------------------------------------------------------------------------------------
void
SignalGuard::setEmit(
    const bool &a_flag
)
{
    _isEmit = a_flag;
}
//-------------------------------------------------------------------------------------------------

} // namespace qtlib
